    private void txtpassword_hashFocusGained(java.awt.event.FocusEvent evt) {                                                                                          
        String pass = new String(txtpassword_hash.getPassword());
        if (pass.equals("Contraseña")) {
            txtpassword_hash.setText("");
            txtpassword_hash.setEchoChar('•');
        }
    }                                                                                        

    private void txtpassword_hashFocusLost(java.awt.event.FocusEvent evt) {                                                                                      
        String pass = new String(txtpassword_hash.getPassword());
        if (pass.isEmpty()) {
            txtpassword_hash.setText("Contraseña");
            txtpassword_hash.setEchoChar((char) 0);
        }    
    }                                                                                    

