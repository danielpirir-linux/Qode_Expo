   private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    String nombre = txtnombre.getText().trim();
    String email = txtEmail.getText().trim();
    String password_hash = new String(txtpassword_hash.getPassword());
    
    // 1. Validar campos vacíos
    if (nombre.isEmpty() || nombre.equals("Nombre de Usuario") ||
        email.isEmpty() || email.equals("Usuario o correo") ||
        password_hash.isEmpty() || password_hash.equals("Contraseña")) {
        
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos obligatorios antes de continuar.");
        return; 
    }

    // 2. Validar sintaxis del correo
    if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
        JOptionPane.showMessageDialog(null, "El correo electrónico ingresado no tiene un formato válido.", "Formato Inválido", JOptionPane.ERROR_MESSAGE);
        return;
    }

    JOptionPane.showMessageDialog(null, "Enviando código de verificación a: " + email + "\nPor favor espere un momento", "Verificando Correo Electrónico", JOptionPane.INFORMATION_MESSAGE);
    
    Clases.ServicioCorreo servicioMail = new Clases.ServicioCorreo();
    String codigoGenerado = servicioMail.generarCodigo();
    
    boolean enviado = servicioMail.enviarCodigoVerificacion(email, codigoGenerado);
    
    if (!enviado) {
        JOptionPane.showMessageDialog(null, "No se pudo enviar el correo electrónico. Verifique su conexión o que el correo emisor esté bien configurado.", "Error de Envío", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String codigoUsuario = JOptionPane.showInputDialog(null, "Hemos enviado un código de 6 dígitos a tu correo.\nIngrésalo aquí para verificar tu cuenta:", "Verificación de Cuenta", JOptionPane.QUESTION_MESSAGE);
    
    if (codigoUsuario == null) {
        JOptionPane.showMessageDialog(null, "Registro cancelado por el usuario.");
        return;
    }

    if (codigoUsuario.trim().equals(codigoGenerado)) {
        Clases.Conexion co = new Clases.Conexion();
        int respuesta = co.usuarios(nombre, email, password_hash);
        
        if (respuesta == 1) {
            JOptionPane.showMessageDialog(null, "¡Cuenta verificada y registrada correctamente!");
            
            String emailMinusculas = email.toLowerCase();
            if (emailMinusculas.contains("admin") || emailMinusculas.contains("mod")) {
CRUD panelCrud = new CRUD(nombre, email); 
panelCrud.setVisible(true);
            } else {
                Prueba_Vocacional pruebaVocacional = new Prueba_Vocacional();
                pruebaVocacional.setVisible(true);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error al escribir en la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(null, "El código introducido es incorrecto. Validación fallida.", "Código Erróneo", JOptionPane.ERROR_MESSAGE);
    }
}                                                   

